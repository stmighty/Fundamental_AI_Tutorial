# math_operations.py

def add(a, b):
    """Add two numbers."""
    return a + b

def multiply(a, b):
    """Multiply two numbers."""
    return a * b

if __name__ == "__main__":
    # This code block will only execute when the script is run directly,
    # not when it is imported as a module in another script.
    print("Demonstrating module functionality:")
    print("Add 2 + 3 =", add(2, 3))
    print("Multiply 2 * 3 =", multiply(2, 3))
